var app = angular.module('wa-ng-proto-starter', ['ui.router','ngAnimate',]);
 
////////////////////////////////////////////////////////////////////
///////////////////// Routing And State Set Up ///////////////////// 
////////////////////////////////////////////////////////////////////
app.config(function($stateProvider, $locationProvider) {

    $locationProvider.html5Mode(true);

    $stateProvider
        ///////////////////////////////// 
        ////// States for Welcome //////
        ///////////////////////////////
        .state('welcome', {
          url: "/",
          views: {
            "main@": {
              templateUrl: "app/views/welcome.html",
            }
          }
        });
});

////////////////////////////////////////////////////////////////////
//////////////////////// Main App Controller /////////////////////// 
////////////////////////////////////////////////////////////////////

app.controller("mainCtrl", function mainCtrl(storeService){
  var vm = this;
  vm.store = storeService; // Make store accessible to component by aliasing it to vm.store
});

////////////////////////////////////////////////////////////////////
//////////////////////// Bootstrap The App /////////////////////////
////////////////////////////////////////////////////////////////////
angular.element(document).ready(function() {
  angular.bootstrap(document, ['wa-ng-proto-starter']);
  console.log('Angular Bootstrapped');
});